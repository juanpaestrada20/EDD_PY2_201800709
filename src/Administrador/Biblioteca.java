    private void btnSearchBooksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchBooksActionPerformed
        // TODO add your handling code here:
        cbxResults.removeAllItems();
        if (!txtBusquedaLibros.getText().isEmpty()) {
            String searchBook = txtBusquedaLibros.getText();
            if (isLong(searchBook)) {
                long code = Long.parseLong(searchBook);
                Libro book = library.searchBooks(code);
                if (book != null) {
                    colorcarCampos(book);
                } else {
                    ArrayList<Libro> libros = library.searchBooksbyName(searchBook);
                    agregarLibros(libros);

                }
            } else {
                ArrayList<Libro> libros = library.searchBooksbyName(searchBook);
                agregarLibros(libros);
            }
        }
    }//GEN-LAST:event_btnSearchBooksActionPerformed

