    private boolean isLong(String numero) {
        try {
            Long.parseLong(numero);
            return true;
        } catch (NumberFormatException e) {
            isInteger(numero);
            return false;
        }
    }

    private boolean isInteger(String numero) {
        try {
            Integer.parseInt(numero);
            return true;
        } catch (NumberFormatException e) {

            return false;
        }
    }

    private void colorcarCampos(Libro book) {
        txtAuthor.setText(book.getAutor());
        txtCategory.setText(book.getCategoria());
        txtEdition.setText(String.valueOf(book.getEdicion()));
        txtEditorial.setText(book.getEditorial());
        txtISBN.setText(String.valueOf(book.getISBN()));
        txtLenguage.setText(book.getIdioma());
        txtTitle.setText(book.getTitulo());
        txtYear.setText(String.valueOf(book.getAnio()));
    }

    private void agregarLibros(ArrayList<Libro> libros) {
        if (libros != null) {
            cbxResults.removeAllItems();
            cbxResults.addItem("Resultado de Busqueda:");
            for (Libro book : libros) {
                cbxResults.addItem(book.getTitulo() + ";" + book.getISBN());
            }
        }
    }

