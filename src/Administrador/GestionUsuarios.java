    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        if (rbAdd.isSelected()) {
            if (verifyData()) {
                String[] valores = obtenerDatos();
                if (valores[4].equals(valores[5])) {
                    Usuario nuevo = new Usuario(Long.valueOf(valores[0]), valores[1], valores[2], valores[3], valores[4]);
                    userTable.insertUser(nuevo);
                    UsuariosAgregados.add(nuevo);
                    JOptionPane.showMessageDialog(null, "Nuevo Usuario Agregado", "Datos Correctos", JOptionPane.INFORMATION_MESSAGE);
                    limpiarDatos();
                    colocarImagen();
                } else {
                    JOptionPane.showMessageDialog(null, "No se puede realizar el registro", "Datos Erroneos", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "No se puede realizar el registro", "Datos faltantes", JOptionPane.ERROR_MESSAGE);
            }
        } else if (rbDelete.isSelected()) {
            int resp = JOptionPane.showConfirmDialog(null, "¿Esta seguro?", "Alerta!", JOptionPane.YES_NO_OPTION);
            if (resp == 0) {
                userTable.deleteUser(Long.valueOf(txtCarnet.getText()));
                JOptionPane.showMessageDialog(null, "Usuario Eliminado", "Usuario Eliminado", JOptionPane.INFORMATION_MESSAGE);
                UsuariosEliminados.add(usuarioLogeado);
                usuarioLogeado = null;
                InicioSesion inicio = new InicioSesion();
                inicio.setVisible(true);
                dispose();

            }
        } else if (rbModify.isSelected()) {
            String[] valores = obtenerDatos();
            if (Long.valueOf(valores[0]) != usuarioLogeado.getCarnet()) {
                JOptionPane.showMessageDialog(null, "No se permite cambiar el valor de carnet", "Cambios no validos", JOptionPane.WARNING_MESSAGE);
                txtCarnet.setText(String.valueOf(usuarioLogeado.getCarnet()));
            } else if (!txtPassword.getText().equals(txtConfirmPass.getText())) {
                JOptionPane.showMessageDialog(null, "No se coinciden las contraseñas    ", "Cambios no validos", JOptionPane.WARNING_MESSAGE);
                txtPassword.setText("");
                txtConfirmPass.setText("");
            } else {
                usuarioLogeado.setNombre(valores[1]);
                usuarioLogeado.setApellido(valores[2]);
                usuarioLogeado.setCarrera(valores[3]);
                usuarioLogeado.setContrasena(valores[4]);
                UsuariosModficados.add(usuarioLogeado);
                JOptionPane.showMessageDialog(null, "Usuario modificado", "Modificacion", JOptionPane.INFORMATION_MESSAGE);
                limpiarDatos();
                colocarImagen();
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

