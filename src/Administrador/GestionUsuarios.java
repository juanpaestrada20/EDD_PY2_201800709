    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Usuario busqueda = userTable.search(Long.valueOf(txtSearchCarnet.getText()));
        if (busqueda != null) {
            txtCarnet.setText(String.valueOf(busqueda.getCarnet()));
            txtName.setText(busqueda.getNombre());
            txtLastName.setText(busqueda.getApellido());
            txtCareer.setText(busqueda.getCarrera());
            txtPassword.setText(busqueda.getMD5());
        } else {
            JOptionPane.showMessageDialog(null, "No se encontro el usuario", "Busqueda", JOptionPane.ERROR_MESSAGE);
            txtSearchCarnet.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private String[] obtenerDatos() {
        String[] valores = new String[6];
        valores[0] = txtCarnet.getText();
        valores[1] = txtName.getText();
        valores[2] = txtLastName.getText();
        valores[3] = txtCareer.getText();
        valores[4] = txtPassword.getText();
        valores[5] = txtConfirmPass.getText();
        return valores;
    }

    private Boolean verifyData() {
        if (txtCarnet.getText().isEmpty() || txtCareer.getText().isEmpty() || txtConfirmPass.getText().isEmpty()
                || txtLastName.getText().isEmpty() || txtName.getText().isEmpty() || txtPassword.getText().isEmpty()) {
            return false;
        } else {
            return true;
        }
    }

    private void habilitarCeldas(Boolean habilitado) {
        txtCarnet.setEditable(habilitado);
        txtName.setEditable(habilitado);
        txtLastName.setEditable(habilitado);
        txtCareer.setEditable(habilitado);
        txtPassword.setEditable(habilitado);
        txtConfirmPass.setEditable(habilitado);
    }

    private void colocarDatosPropios() {
        txtCarnet.setText(String.valueOf(usuarioLogeado.getCarnet()));
        txtName.setText(usuarioLogeado.getNombre());
        txtLastName.setText(usuarioLogeado.getApellido());
        txtCareer.setText(usuarioLogeado.getCarrera());
        txtPassword.setText(usuarioLogeado.getContrasena());
    }

    private void limpiarDatos() {
        txtCarnet.setText("");
        txtName.setText("");
        txtLastName.setText("");
        txtCareer.setText("");
        txtPassword.setText("");
        txtConfirmPass.setText("");
    }

