    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        long carnetLogin = Long.parseLong(txtCarnet.getText());
        Usuario userLogin = userTable.search(carnetLogin);
        usuarioLogeado = null;
        if (userLogin == null) {
            System.out.println("No se encontro el usuario");
            JOptionPane.showMessageDialog(null, "Usuario y/o Contrase√±a Incorrecta", "Credenciales Inv√°lidas", JOptionPane.ERROR_MESSAGE);
        } else {
            String passwordLogin = String.valueOf(txtPassword.getPassword());
            if (userLogin.getContrasena().equals(passwordLogin)) {
                System.out.println("Bienvenido " + userLogin.getNombre());
                usuarioLogeado = userLogin;
                MenuPrincipal menu = new MenuPrincipal();
                menu.setVisible(true);
                dispose();
                
            } else {
                JOptionPane.showMessageDialog(null, "Usuario y/o Contrase√±a Incorrecta", "Credenciales Inv√°lidas", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

