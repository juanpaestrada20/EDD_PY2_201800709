    private void ChargeUser() {
        chooser = new JFileChooser();
        if (chooser.showDialog(jPanel1, "Abrir") == JFileChooser.APPROVE_OPTION) {
            file = chooser.getSelectedFile();
            if (file.canRead()) {
                if (file.getName().endsWith("json")) {
                    readJson(file);
                } else {
                    JOptionPane.showMessageDialog(jPanel1, "Debe abrir Archivo Json", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }

    private void readJson(File file) {
        try {
            Object obj = new JSONParser().parse(new FileReader(file));

            JSONObject jsonObject = (JSONObject) obj;

            JSONArray usuarios = (JSONArray) jsonObject.get("Usuarios");

            Iterator<JSONObject> iterator = usuarios.iterator();

            while (iterator.hasNext()) {
                JSONObject user = iterator.next();
                long carnet = (long) user.get("Carnet");
                String firstName = (String) user.get("Nombre");
                String lastName = (String) user.get("Apellido");
                String career = (String) user.get("Carrera");
                String pass = (String) user.get("Password");
                Usuario nuevo = new Usuario(carnet, firstName, lastName, career, pass);
                System.out.println(carnet + " - " + firstName + " - " + pass);
                UsuariosAgregados.add(nuevo);
                userTable.insertUser(nuevo);
            }
            userTable.generateTable();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(InicioSesion.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(InicioSesion.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(InicioSesion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

